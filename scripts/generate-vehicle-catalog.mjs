import { promises as fs } from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve('public/Assets/Vehicles/Player');
const QUALITY_DIRS = [
  ['hd', 'HD'],
  ['pixel', 'Pixel'],
];
const OUT_FILE = path.resolve('src/data/vehicles.generated.js');
const STRICT = process.argv.includes('--strict');
const VALID_COLORS = new Set(['white', 'yellow', 'blue', 'red', 'green', 'base']);

function parseVehicleParts(filename) {
  // Naming convention:
  // - Vehicles_<CARID>_<...>.png (legacy/base)
  // - Vehicles_<CARID>_<COLOR>_<...>.png (recommended for color scan)
  const match = filename.match(/^Vehicles_([^_]+)_([^/]+)\.png$/i);
  if (!match) return null;
  const carId = String(match[1]).toUpperCase();
  const suffix = String(match[2]);
  const token = suffix.split('_')[0]?.toLowerCase() || 'base';
  const reserved = new Set(['hd', 'pixel', 'base']);
  const color = reserved.has(token) ? 'base' : token;
  return { carId, color };
}

async function listPngs(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith('.png'))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function validateFileName(file, issues) {
  if (file.toLowerCase().endsWith('.png.png')) {
    issues.push(`double extension détectée: ${file}`);
  }
}

async function main() {
  const variantsByVehicle = new Map();
  const issues = [];

  for (const [qualityKey, qualityDir] of QUALITY_DIRS) {
    const dir = path.join(ROOT, qualityDir);
    const files = await listPngs(dir);

    for (const file of files) {
      validateFileName(file, issues);

      const parsed = parseVehicleParts(file);
      if (!parsed) {
        issues.push(`nom ignoré (pattern invalide) dans ${qualityDir}: ${file}`);
        continue;
      }

      const { carId, color } = parsed;
      if (!VALID_COLORS.has(color)) {
        issues.push(`couleur non standard détectée (${color}) pour ${qualityDir}/${file}`);
      }

      const next = variantsByVehicle.get(carId) || {};
      const qualityMap = next[qualityKey] || {};
      qualityMap[color] = `/Assets/Vehicles/Player/${qualityDir}/${file}`;
      next[qualityKey] = qualityMap;
      variantsByVehicle.set(carId, next);
    }
  }

  const sorted = Object.fromEntries(
    [...variantsByVehicle.entries()]
      .sort((a, b) => a[0].localeCompare(b[0]))
      .map(([id, variants]) => [id, variants]),
  );

  const lines = [
    '// Generated by scripts/generate-vehicle-catalog.mjs',
    `export const GENERATED_VEHICLE_VARIANTS = ${JSON.stringify(sorted, null, 2)};`,
    '',
  ];
  await fs.writeFile(OUT_FILE, lines.join('\n'), 'utf8');

  for (const [id, variants] of Object.entries(sorted)) {
    const missing = QUALITY_DIRS.map(([key]) => key).filter((key) => !variants[key]);
    if (missing.length) {
      issues.push(`${id}: qualité manquante -> ${missing.join(', ')}`);
      console.log(`${id}: missing ${missing.join(', ')}`);
    } else {
      console.log(`${id}: ok`);
    }
  }

  if (issues.length) {
    console.warn('\n[vehicle:scan] anomalies détectées:');
    issues.forEach((issue) => console.warn(`- ${issue}`));
  }

  if (STRICT && issues.length) {
    console.error(`\n[vehicle:scan] strict mode: ${issues.length} anomalie(s), arrêt avec code 1.`);
    process.exit(1);
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
